var subject = '자바스크립트';
switch (subject) {
    case 'C언어':
        console.log('c언어 입니다.');
        break;
    case '자바스크립트':
        console.log('자바스크립트 입니다.');
        break;
    case '파이썬':
        console.log('파이썬 입니다.');
        break;
    default:
        console.log('아무것도 아닙니다.');
        break;
}